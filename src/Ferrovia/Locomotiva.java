package Ferrovia;

public class Locomotiva {

	private char locomotiva;
	
	public Locomotiva() {
		
	}

	public Locomotiva(char locomotiva) {
		super();
		this.locomotiva = locomotiva;
	}

	public char getLocomotiva() {
		return locomotiva;
	}

	public void setLocomotiva(char locomotiva) {
		this.locomotiva = locomotiva;
	}
		
}
